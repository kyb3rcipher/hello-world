    public Utilidades() {
        initComponents();
        setTitle("Utilidades - Kyb3r Cipher");
        setLocationRelativeTo(null);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        
        // Code
        meses = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingresa tus meses trabajados: "));
        salario = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingresa tus salario: "));
        // Calcular utilidad
        utilidad = (float) (
                (meses < 12) ? salario * 0.5 :
                (meses >= 12 && meses < 24) ? salario * 0.7 :
                (meses >= 24 && meses < 60) ? salario * 0.10 :
                (meses >= 60 && meses < 120) ? salario * 0.15
                    : salario * 0.20);
        jLabel1.setText("Tu utilidad es de: " + utilidad);
    }

