    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int terms, oldNumber = 0, nextNumber = 1, fibonacciNumber;
        terms = Integer.parseInt(textfieldTerms.getText());
        String fibonacci = "";
        
        if (terms == 0 || textfieldTerms.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Set a positivive integer more than 0", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (terms == 1) {
            fibonacci += oldNumber;
        } else {
            int count = 0;
            while (count < terms) {
                fibonacci += oldNumber + " ";
                fibonacciNumber = oldNumber + nextNumber;
                
                oldNumber = nextNumber;
                nextNumber = fibonacciNumber;
                count++;
            }
        }
        
        // Set fibonacci
        textArea.setText(fibonacci);
    }//GEN-LAST:event_jButton1ActionPerformed

