# RELATIONAL OPERATORS
# < > <= >= == !=

# menor que
print("1 es menor que 5?:", 1 < 5)
# mayor que
print("1 es mayor que 5?:", 1 > 5)

# mayor igual que
print("\n6 es mayor o igual que 5?:", 6 >= 5)
# mayor igual que
print("6 es menor o igual que 5?:", 6 <= 5)

# igual que
print("\n3 es igual que 3?:", 3 == 3)
# no igual que
print("3 no es igual que 3?:", 3 != 3)
